static void HX8347Execute(void)
{   
    ulSysClk = xSysCtlClockGet();   
    //
    // LCD Clear.
    //
    HX8347LCDClear(Green);
    xSysCtlDelay(ulSysClk/5);
    HX8347LCDClear(White);   
    //
    // Change background color and text color,display string
    //
    HX8347SetBackColor(Blue);
    HX8347SetTextColor(White);
    HX8347DisplayString(0, 0, "  STM32F1xx Board   ");
    
    HX8347SetBackColor(Blue);
    HX8347SetTextColor(Red);
    HX8347DisplayString(1, 0, "  HX8347-G EXAMPLE  ");  
    //
    // Display number.
    //
    HX8347SetBackColor(White);
    HX8347SetTextColor(Red);
    HX8347DisplayN(8,16,2012); 
    //
    // Display Bmp.
    //
    HX8347DisplayBmp(0, 50, 120, 45, Bmp);
    xSysCtlDelay(ulSysClk/5);
    //
    // Power off display and then power on display.
    //
    HX8347DisplayOff();
    xSysCtlDelay(ulSysClk/5);
    HX8347DisplayOn();
    //
    //  Display chinese characters.
    HX8347SetBackColor(White);
    HX8347SetTextColor(Black);
    HX8347DisplayChinese(10,10,7,Ch);
}
